import sys

xor = [0x76, 0x58, 0xb4, 0x49, 0x8d, 0x1a, 0x5f, 0x38, 0xd4, 0x23, 0xf8, 0x34, 0xeb, 0x86, 0xf9, 0xaa]
add = [0xef, 0xbe, 0xad, 0xde, 0xad, 0xde, 0xe1, 0xfe, 0x37, 0x13, 0x37, 0x13, 0x66, 0x74, 0x63, 0x67]
shuffle = [0x2, 0x6, 0x7, 0x1, 0x5, 0xB, 0x9, 0xE, 0x3, 0xF, 0x4, 0x8, 0xA, 0xC, 0xD, 0x0]

def forward(char, index):
    new_index = shuffle.index(index)
    add_result = (char + add[new_index]) % 256
    xor_result = add_result ^ xor[new_index]
    return xor_result, new_index

char, index = forward(int(sys.argv[1], 16), int(sys.argv[2]))
print(hex(char), chr(char), index)
